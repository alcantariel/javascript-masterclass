// math api - min, max e random

console.log(Math.min(1, 2, 3, 4, 5, 6)); // 1
console.log(Math.max(1, 2, 3, 4, 5, 6)); // 6
console.log(Math.random()); // random entre 0 e 1
console.log(Math.random() * 10); // random entre 0 e 10
console.log(Math.random() * 100); // random entre 0 e 100
console.log(Math.random() * 1000); // random entre 0 e 1000
