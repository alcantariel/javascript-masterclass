// number operators - operadores de incremento e decremento

let result = 10;
console.log(result++); // 10 - pós incremento
console.log(result); // 11
console.log(++result); // 12 - pré incremento

console.log(result--); // 12 - pós decremento
console.log(result); // 11
console.log(--result); // 10 - pré decremento

// pré: incrementa/decrementa e depois retorna
// pós: retorna e depois incrementa/decrementa
