// number operators - operadores de atribuição

let resultSum = 10;
let resultSub = 10;
let resultMul = 10;
let resultDiv = 10;
let resultRem = 10;

resultSum += 2; // resultSum = resultSum + 2
resultSub -= 5; // resultSub = resultSub - 5
resultMul *= 2; // resultMul = resultMul * 2
resultDiv /= 2; // resultDiv = resultDiv / 2
resultRem %= 3; // resultRem = resultRem % 3

console.log(resultSum); // 12
console.log(resultSub); // 5
console.log(resultMul); // 20
console.log(resultDiv); // 5
console.log(resultRem); // 1
