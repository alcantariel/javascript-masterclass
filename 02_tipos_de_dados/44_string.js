// String API

console.log("JavaScript".slice(0, 4)); // Java - (0 até n - 1)
console.log("JavaScript".slice(4)); // Script - length = 6
console.log("JavaScript".slice(0, -6)); // Java - sai de 0 e o -6 é referente a contagem da String de trás pra frente
console.log("JavaScript".slice(-6)); // Script
console.log("C;Java;JavaScript;Ruby".split(";")); // ['C', 'Java', 'JavaScript', Ruby]
console.log("JavaScript".substring(0, 4)); // Java - (0 até n - 1)
console.log("JavaScript".substring(4, 0)); //  Java - inverte a ordem dos parametros se o primeiro for maior que o segundo
console.log("JavaScript".substring(4)); // Script
