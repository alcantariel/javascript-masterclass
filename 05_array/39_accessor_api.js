// Accessor API - join

const languages = ["Smalltalk", "C++", "Java", "Haskell", "Scheme"];

console.log(languages.join(";")); // Smalltalk;C++;Java;Haskell;Scheme
console.log(languages.join(",")); // Smalltalk,C++,Java,Haskell,Scheme
console.log(languages.join(" ")); // Smalltalk C++ Java Haskell Scheme
